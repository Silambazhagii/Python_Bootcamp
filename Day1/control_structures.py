# if-else
score = 85
if score > 90:
    print("A grade")
elif score > 75:
    print("B grade")
else:
    print("C grade")

# for loop
for i in range(1, 6):
    print("Count:", i)

# while loop
n = 5
while n > 0:
    print("n is", n)
    n -= 1